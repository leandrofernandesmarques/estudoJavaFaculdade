package cubo;

public class volumeCubo {

	public static void main(String[] args) {
		
		int arestaDam = 3;
		int volumeDamCubico = arestaDam * arestaDam * arestaDam;
		
		System.out.println("O volume do Cubo é " + volumeDamCubico + " Dam³");
		System.out.println("Logo o volume em Decimetro é " + volumeDamCubico * 1000000 + " Dm³");

	}

}
